#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	nbr;

	if (n == -2147483648)
	{
		n = n * (-1);
		write(fd, "-2", 2);
		n = 147483648;
	}
	if (n < 0)
	{
		n = n * (-1);
		write(fd, "-", 1);
	}
	nbr = (n % 10) + '0';
	n = n / 10;
	if (n > 0)
		ft_putnbr_fd(n, fd);
	write(fd, &nbr, 1);
}
